# AccessIQ — Lema del programa (JoaoMax)
#
# AccessIQ nace para que registrar la asistencia deje de ser un trámite y pase a ser
# una herramienta clara. Pues, detrás de cada DNI hay una persona con responsabilidades, metas
# y un horario que cumplir; además, llegar a tiempo no es solo cumplir una norma, es una forma
# de respeto hacia el equipo y hacia el propio trabajo. Este sistema acompaña ese
# compromiso registrando cada jornada con precisión, para que la puntualidad deje de
# pasar desapercibida y se convierta en un hábito visible. ya que además, cada entrada queda
# documentada, cada reporte habla por sí solo, y quien administra el equipo puede
# consultar sin enredos, tomar decisiones con datos reales y mantener un registro
# honesto de quién estuvo presente y cuándo.
